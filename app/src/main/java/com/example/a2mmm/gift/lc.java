public class lc {
    private String name;
    private String code;
    private String bfb;


    public lc() {
    }

    public lc(String name, String code, String bfb) {
        this.name = name;
        this.code = code;
        this.bfb = bfb;

    }

    public String getname() {
        return name;
    }

    public String getcode() {
        return code;
    }
    public String getbfb()  {
        return bfb;
    }






}
